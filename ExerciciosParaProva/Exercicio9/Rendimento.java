package ExerciciosParaProva.Exercicio9;

public class Rendimento {

    private double capital;
    private double Selic;
    private double TaxaAdministrativa;
    private double imposto;
    private double total;
    private double rendimento;
    
    public double getCapital() {
        return capital;
    }
    public void setCapital(double capital) {
        this.capital = capital;
    }
    public double getSelic() {
        return Selic;
    }
    public void setSelic(double selic) {
        Selic = selic;
    }
    public double getTaxaAdministrativa() {
        return TaxaAdministrativa;
    }
    public void setTaxaAdministrativa(double taxaAdministrativa) {
        TaxaAdministrativa = taxaAdministrativa;
    }
    public double getImposto() {
        return imposto;
    }
    public void setImposto(double imposto) {
        this.imposto = imposto;
    }
    public double getTotal() {
        return total;
    }
    public void setTotal(double total) {
        this.total = total;
    }
    public double getRendimento() {
        return rendimento;
    }
    public void setRendimento(double rendimento) {
        this.rendimento = rendimento;
    }

    
    
}


