/*
 * Linguagens de programação podem ser compiladas e interpretadas, explique o
 * conceito de cada
 * contexto ressaltando suas diferenças. Em relação à Linguagem Java, em qual
 * desses contextos se
 * encaixa?
 * 
 * 
 * Compilação: Quando você escreve código Java, ele é compilado pelo compilador
 * Java (geralmente javac) em bytecode. O bytecode é um código intermediário que
 * não é específico para uma plataforma de hardware em particular.
 * 
 * Interpretação: Em seguida, o bytecode é executado por uma máquina virtual
 * Java (JVM - Java Virtual Machine). A JVM interpreta o bytecode e o executa na
 * máquina de destino. Isso permite que o código Java seja executado em qualquer
 * máquina que tenha uma JVM compatível, tornando-o independente de plataforma.
 * 
 *
 * Interpretação:
 * 
 * Em um contexto de linguagem interpretada, o código fonte não é traduzido em
 * código de máquina antes da execução.
 * Em vez disso, um programa chamado interpretador lê o código fonte linha por
 * linha e executa as instruções diretamente.
 * Isso significa que o código-fonte é traduzido e executado sob demanda, uma
 * linha de cada vez.
 * Como resultado, os erros no código podem ser detectados e relatados enquanto
 * o programa está sendo interpretado.
 */

/*
 * Classe:
 * 
 * Uma classe é uma estrutura ou um plano que descreve um conjunto de objetos
 * que compartilham características comuns.
 * Ela age como um modelo ou um projeto para criar objetos.
 * A classe define os atributos (variáveis de instância) que representam
 * características do objeto e os métodos (funções) que definem o comportamento
 * do objeto.
 * Exemplos de classes podem ser "Carro", "Pessoa", "Conta Bancária", etc.
 * As classes são a abstração do mundo real e definem como os objetos de um
 * determinado tipo devem ser criados e interagem entre si.
 * 
 * 
 * Objeto:
 * 
 * Um objeto é uma instância concreta de uma classe. Em outras palavras, é uma
 * entidade que foi criada com base na classe.
 * Um objeto possui estados (valores de atributos) e comportamento (métodos)
 * associados a ele.
 * Por exemplo, se temos a classe "Carro", um objeto dessa classe poderia ser um
 * carro específico com uma marca, modelo, cor, velocidade atual, etc.
 * Os objetos são usados para representar entidades do mundo real no código,
 * permitindo-nos manipular dados e executar ações relacionadas a essas
 * entidades.
 * 
 */